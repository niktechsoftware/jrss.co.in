                <h3> Constabulary Role</h3>
                <p>The increasing incidence of maritime crime has brought into sharp   focus the constabulary role that navies have to perform. The   significance of this role may be gauged from the fact that for a third   of the world&rsquo;s navies, this is a major facet of their functions. In the   constabulary role, forces are employed to enforce law of the land or to   implement a regime established by an international mandate. Force is   only employed for self-defence or as a last resort in execution of this   role. The protection and promotion of India&rsquo;s maritime security is one   of the prime responsibilities of the Indian Navy. This includes a   constabulary element, especially where it relates to threats that   involve use of force at sea. The range of tasks that the IN has to   undertake in the constabulary role ranges from Low Intensity Maritime   Operations (LIMO) to maintaining good order at sea. This further   includes aspects of coastal security, as part of India&rsquo;s overall   maritime security. Constabulary tasks at sea are neither the primary nor   the sole mandate of the IN. With the establishment of the ICG in   February 1978, law enforcement aspects of the constabulary role within   the Maritime Zones of India (MZI) have been transferred to the ICG.28   Security in major harbours and ports are the purview of the port   authorities, aided by customs and immigration agencies. Constabulary   tasks beyond the MZI are vested with the Indian Navy. Efficient and   effective maritime constabulary requires proper and seamless   coordination between the various maritime law enforcement and regulatory   agencies. After the terrorist attacks on Mumbai on 26 November 2008,   the overall responsibility for coastal security has been mandated to the   Indian Navy, in close coordination with the ICG, State marine police   and other central/state government and port authorities.</p>

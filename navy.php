                <h3> The Diplomatic Role</h3>
                <p>Naval Diplomacy entails the use of naval forces in support of foreign   policy objectives to build &lsquo;bridges of friendship&rsquo; and strengthen   international cooperation on the one hand, and to signal capability and   intent to deter potential adversaries on the other. The larger purpose   of the navy&rsquo;s diplomatic role is to favourably shape the maritime   environment in the furtherance of national interests, in consonance with   the foreign policy and national security objectives. Navies inherently   lean towards performing a diplomatic role on account of two   characteristics. The first is their status as comprehensive instruments   of a country&rsquo;s sovereign power, whereupon their very presence in or off a   certain area signals the nation&rsquo;s political intent and commitment to   pursue national interests in that region. Hence, their presence or   absence can be calibrated to send a political message to potential   friends and foes alike. The second characteristic facilitating the   navy&rsquo;s diplomatic role is in the very attributes of maritime forces,   including access, mobility, sustenance, reach, flexibility and   versatility. These combine to offer a variety of tools for furthering   national interests and pursuing foreign policy goals. Naval forces can   be readily deployed, can perform multiple roles and tasks that can be   calibrated in visibility and intensity as per requirements, and can just   as easily and rapidly be withdrawn, to send a countersignal.</p>

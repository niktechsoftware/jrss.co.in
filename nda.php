<p style="text-align:justify;"><strong>NDA Selection Procedure Details:</strong>Union Public Service Commission – UPSC conducts NDA (National Defence Academy) exam, NDA-I, NDA-II . The candidates who are eligible apply through online and offline application mode. Minimum Educational Qualification is 12 th Class pass in 10+2 Pattern. The selection process is based on Written Test & Interview . The details are as follows… </p>
<h3>Selection Procedure:</h3>
UPSC conducts  NDA (National Defence Academy)  Exam in Two Stages.
<ul>
	<li>I. Written Examination</li>
	<li>II. Interview</li>
</ul>
<p style="text-align:justify;"><strong>I. Written Examination:</strong>The exam will constitute 2 subjects, Maths and General Ability Test and each subject is given the time limit 2 ½ Hours, maximum marks for
<ul>
    <li>Maths is 300 & </li>
    <li>General ability is 600.</li>
    <li>The papers in all the subjects will consist of objective type questions only. The question papers of Mathematics & Part B of General Ability Test will be set bilingually in Hindi as well as English.</li>
</ul>
</p>
<p style="text-align:justify;"><strong>II. Interview:</strong>Qualified candidates in the written examination will be called for Interview conducted by SSB (Services selection board). For Army, Navy Wings & 10 +2 Entry Scheme candidates need to undergo Intelligence Test & Personality Test, but for Air Force Wing the candidates must attend a Pilot Aptitude Test. The Interview Consists both Intelligence and Personality Test. In interview the candidates will be put to Intelligence Tests both Verbal and Non Verbal Tests based knowledge to assess their intelligence, they will also go to Group Tests such as Group Discussions, Group Planning, Outdoor Group Tasks, and asked to give Brief Lectures on Specified Subjects.</p>

<p style="text-align:justify;"><strong>Two stage selection procedure</strong>The selection procedure based on Psychological Aptitude Test and Intelligence Test. Candidates who qualified in stage 1. are required to appear stage 2. The qualified candidates need to submit all the Original certificates .<br/>

A single combined list is prepared on the basis of total marks secured by them in the Written Examination and the Services Selection Board Tests. The final selection based on the number of vacancies for Army, Navy, Air Force of with reference to their order in the event of their final selection to one service will not admit to other remaining services.</p>

<p style="text-align:justify;"><strong>N.B:</strong>Air force given pilot aptitude test only once for the candidate’s .The test selected candidates will go to every subsequent Interview.</p>

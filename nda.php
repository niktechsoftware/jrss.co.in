							NDA (National Defence Academy) is a academy where Army, Navy &amp; Air force cadets are trained together for Indian Armed Forces before they go on to pre-commissioning training to their respective service academies. The National Defence Academy is a Joint Services academy of the Indian Armed Forces, where cadets of the three services, the Army, the Navy and the Air Force are trained together before they go on to pre-commissioning training to their respective service academies. It is located in Khadakwasla near Pune, Maharashtra.It was the 1st tri-service academy in the world and is rated amongst the best in the world.</p>
